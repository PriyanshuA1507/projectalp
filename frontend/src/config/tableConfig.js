const APP_BASE_PATH = '/app';

const prefixPath = (path) => {
  if (!path || typeof path !== 'string') {
    return path;
  }

  if (path.startsWith(APP_BASE_PATH)) {
    return path;
  }

  if (path.startsWith('/')) {
    return `${APP_BASE_PATH}${path}`;
  }

  return `${APP_BASE_PATH}/${path}`;
};

